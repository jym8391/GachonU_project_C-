#include <bits/stdc++.h>
using namespace std;

int main()
{
    int number;
    scanf("%d",&number);
    if(number==1) printf("Samsung");
    else if(number==2) printf("LG");
    else if(number==3) printf("Apple");
    else if(number==4) printf("Google");
    else printf("invalid code");
    return 0;
}